package lab8;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Аннотация @DataProcessor - это как "наклейка" на методах
 *
 * Представь, что у тебя есть куча разных коробок (методов) на складе.
 * Ты хочешь пометить только те коробки, в которых лежат инструменты для обработки данных.
 * Вместо того чтобы писать на каждой коробке "ЭТО ИНСТРУМЕНТ", ты просто клеишь на них
 * специальную наклейку @DataProcessor.
 *
 * Потом ты можешь легко найти все коробки с этой наклейкой и понять, что в них инструменты!
 *
 * @Retention(RetentionPolicy.RUNTIME) - эта наклейка не стирается и видна даже когда программа работает
 * @Target(ElementType.METHOD) - эту наклейку можно клеить только на методы (не на классы, не на поля)
 */
@Retention(RetentionPolicy.RUNTIME)  // Аннотация будет доступна во время выполнения программы
@Target(ElementType.METHOD)          // Аннотацию можно применять только к методам
public @interface DataProcessor {

    /**
     * Описание того, что делает этот обработчик данных
     *
     * Это как подпись на наклейке - можно написать "Фильтр" или "Сортировщик"
     * По умолчанию будет пустая строка, если не укажем
     */
    String description() default "";

    /**
     * Приоритет выполнения обработчика (чем выше число, тем раньше выполнится)
     *
     * Представь очередь в магазине: кто с приоритетом 100 - идет первым,
     * кто с приоритетом 50 - вторым, и т.д.
     */
    int priority() default 0;
}
